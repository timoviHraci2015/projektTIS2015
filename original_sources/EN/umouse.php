	<H2>Micromouse category</H2>
	</br>
	<H3>Introduction</H3>

	<P>
		A Micromouse is a small microprocessor controlled vehicle that is able to navigate its way through an unknown and unconnected maze. 
		Our rules are adapted from the international micromouse competitions. 
	</P>

	<H3>The competition task</H3>

	<P>
		The objective for the contestant is to impart to the micromouse an adaptive intelligence to explore different maze configurations and to 
		work out the optimum route for the shortest travel time from start to finish. 
	</P>
	
	<H3>Maze</H3>
	
	<p>
		The maze shall comprise 16 x 16 multiples of an 18 cm x 18 cm unit square. The walls constituting the maze shall be 5 cm high and 1.2 cm thick. 
		Passageways between the walls shall be 16.8 cm wide. The outside wall shall enclose the entire maze. 
	</p>
	
	<p>
		The side of the maze walls shall be white, and the top of the walls shall be red. The floor of the maze shall be made of wood and finished with a 
		non-gloss black paint. The coating on the top and side of the wall shall be selected to reflect infra-red light and the coating on the floor shall absorb it. 
	</p>
	
	<p>
		he start of the maze shall be located at one of the four corners. 
		At the centre of the maze shall be a large opening which is composed of 4 unit squares. This central square shall be destination. 
	</p>
	
	<p>
		Square posts, each 1.2cm x 1.2cm x 5cm high, at the four corners of each unit square are called lattice points. 
		The maze shall be constituted such that there is at least one wall touching each lattice point, except for the destination square. 
	</p>
	
	<p>
		The dimensions of the maze shall be accurate to within 5% or 2cm, whichever is less. Assembly joints on the maze floor shall not involve steps of greater than 0.5mm.
		The change of slope at an assembly joint shall not be greater than 4 degrees. Gaps between the walls of adjacent squares shall not be greater than 2 mm. 
	</p>
	
	<p>
		Multiple paths to the destination square are allowed and are to be expected (see <A Name="priklady"></A><A HREF="index.php?page=rules&type=example">examples</A>). 
	</p>
	
	<H3>Mice</H3>

	<p>
		Mice must be completely self-contained and must receive no outside assistance. 
	</p>
	
	<p>
		The method of propulsion is at the discretion of the builder, provided that the 
		power source is non-polluting - internal combustion engines would probably be disqualified on this count.  
	</p>
	
	<p>
		Although the superstructure of the mice may 'bulge' above the top of the maze walls, 
		mice must be subject to the following size constraints - width 25 cm, length 25 cm. There is no height limit. 
	</p>
	
	<p>
		The method of wall sensing is at the discretion of the builder; however, the mouse must not exert a force on any wall likely to cause damage. 
	</p>

	<p>
		If the judges consider that a mouse has a high risk of damaging or sullying the maze they will not permit it to run. Nothing may be deposited in the maze. 
		The mouse must negotiate the maze; it must not jump over, climb, scratch, damage or destroy the walls of the maze. No bulldozers, please. 
	</p>
	
	<H3>Competition Rules</H3>

	<p>
		The time taken to travel from the start square to the destination square is called the 'run' time. 
		Travelling from the destination square back to the start square is not considered a run. 
		The total time taken from the first activation of the micromouse until the start of each run is also measured and it is limited to 5 minutes. 
		If the micromouse requires any manual assistance at any time during the contest, it is considered 'touched' and its 'run' time is penalized with 3 seconds. 
		Scoring is based on these parameters. 
	</p>
	
	<p>
		Each mouse is allowed a maximum of 5 minutes to perform. The judges have the discretion to request a mouse to retire early if by its 
		lack of progress it has become boring, or if by erratic behaviour it is endangering the state of the maze. 
	</p>
	
	<P>
		The scoring of a micromouse shall be obtained by computing a time score for each run as follows: </br>
		Handicapped Time Score = Run Time + Touch Penalty where, Touch Penalty = 3 seconds, if the mouse has been touched at any time prior to the run. 	
	</P>
	
	<p>
		Fastest time score counts for a mouse. 
	</p>
	
	<p>
		When the mouse reaches the destination square, it may stop and remain at the maze centre, or it may continue to explore other parts of the maze, 
		or make its own way back to the start. If the mouse chooses to stop at the centre, it may be lifted out, manually, and restarted by the handler. 
		Manually lifting it out shall be considered touching the mouse and will cause a touch penalty to be added on all subsequent runs. 
		If the mouse does not choose to remain in the destination square, it may not be stopped manually and restarted.
	</p>
	
	<p>
		The time for each run (run time) shall be measured from the moment the mouse leaves the start square until it enters the destination square. 
		The total time on the maze (maze or search time) shall be measured from the time the mouse is first activated. 
	</p>
	
	<p>
		The time taken to negotiate the maze shall be measured either manually by the contest officials, or by infra-red sensors set at the start and destination.  
	</p>
	
	<p>
		The starting procedure of the mouse shall be simple and must not offer a choice of strategies to the handler. 
		For example, a decision to make a fast run to the centre as time runs out must be made by the mouse itself. 
		The starting procedure shall be submitted to the judges when the mouse is registered on the day of the contest. 
	</p>
	
	<p>
		The mouse handler is given 1 minute, from the moment the mouse is taken out of the cage, to make any adjustments (if any) to the mouse sensors. 
		However, no se lection of strategies must be made and no information on the maze configuration entered or captured into the memory. 
	</p>
	
	<p>
		The maze or search time dock will commence after the expiry of the 1 minute time limit even if the handler is still making adjustments to the sensors. 
	</p>
	
	<p>
		If a mouse 'gets into trouble' the handlers can ask the judge for permission to abandon the run and restart the mouse at the beginning. 
		A mouse may not be re- started merely because it has taken a wrong turning - the judges' decision is final. The judges may add a time penalty for a restart. 
	</p>
	
	<p>
		If any part of a mouse is replaced during its performance - such as batteries or EPROMs - or if any significant adjustment is made, then the memory of 
		the maze within the mouse must be erased before re-starting. Slight manipulations of sensors will probably be condoned, but operation of speed or strategy 
		controls expressly forbidden without a memory erasure. It is assumed that the mice will have software stored in EPROMs. However, at the judges' discretion, 
		but not in normal circumstances, mice with battery backed up RAM may be allowed to download control software if the memory is erased accidentally during a run. 
		The handlers, in this instance, must convince the judges that the original software has been reloaded. 
	</p>
	
	<p>
		If no successful run has been made, the judge will make a qualitative assessment of the mouse's performance, based on distance achieved, 
		'purposefulness' versus random behaviour and quality of control. 
	</p>
	
	<p>
		The judges will use their discretion to award the prizes, which in addition to the major prizes may include prizes for specific classes of mouse - 
		perhaps lowest cost, most ingenious, best presented, etc. 
	</p>
	
	<p>
		Under normal circumstances, no part of the mouse may be transferred to another mouse. However, the judges may allow a change of batteries or controller 
		in exceptional cases, if due to accidental damage. Thus, if one chassis is used with two alternative controllers, then they are the same mouse and must 
		perform within a single 5 minutes allocation. The memory must be cleared with the change of controller. 
	</p>
	
	<H3>The competition and the order of the competitors</H3>

	<p>
		The competitors must be prepared to the start within 1 minute after the call. 
		If the competitor does not present himself within 1 minute after the call, the run is lost. 
	</p>

	<H3>Time limits</H3>

	<p>
		The robot has 5 minutes to complete the path. During this time, it can try to find more than one path. Maximum number of runs is limited to 10. 
	</p>

	<H3>Evaluation and prices</H3>

	<p>
		The winner is the robot with the shortest Handicapped Time Score. 
	</p>

	
