	<H2>Pravidlá kategórie </BR></BR>V sklade kečupu </H2>
	<h3>Súťažná úloha</h3>
	<P>
		Navrhnúť a zostrojiť mikropočítačom riadený autonómny mobilný robot, ktorý dokáže správne usporiadať konzervy s paradajkovým pretlakom v sklade. 
		Súťaží vždy dvojica robotov, vyhráva ten, ktorý rýchlejšie a dokonalejšie splní úlohu. 
	</P>
	
	<h3>Kečup</h3>
	<P>
		Úlohou robotov je dopraviť čo najviac kečupu na svoju domácu čiaru.		
	</P>
	
	<P>
		Kečup je v oceľovej pocínovanej plechovke s priemerom 53 mm (+/- 1 mm) a výškou 74 mm (+/- 1 mm). 
		Hmotnosť plechovky s obsahom je približne 163 gramov (+/- 5 g). 
	<P Align="center">
		<IMG Src="./images/KetchupCan.jpg" Width=300 Alt=""></br>
		<I>Paradajkový pretlak v plechovke. Dostupné v bežných potravinách.</I>
	</P>

	<h3>Sklad</h3>

	<P>
		Sklad je reprezentovaný sieťou 5 vodorovných a 5 zvislých čiar s rozostupom 30 cm (+/-1cm). Vodorovné čiary sú očíslované 1-5, zvislé sú označené písmenami A-E. 
		Čiary sú čierne, ich šírka je 15 mm (+/- 1mm). Okolo skladu bude voľná plocha najmenej 30 cm z každej strany. 
	</P>
	
	<P>
		Podklad je biely, vodorovný. Podlaha bludiska bude z plastu, pogumovanej textílie alebo podobného materiálu. 
		Ak podlaha nebude jednoliata, tak spoje na podlahe by nemali vytvárať schodíky vyššie než 1 mm. Zmeny sklonu by nemali byť väčšie ako 4 stupne. 

	</P>
	
	<P>
		Pri štarte stoja roboty v priesečníkoch A3 a E3. Zvislá čiara A je domáca čiara prvého robota, zvislá čiara E je domáca čiara druhého robota. 
	</P>
	
	<P>
		V hre sú 4 kečupy. Na začiatku hry budú vždy dva kečupy na pozíciach C2 a C4. 
		Ďalšie dva budú na niektorej z pozícií B2, B3, B4 resp. D2, D3, D4 stredovo symetricky. Tieto dve pozície sú vybrané pred každým kolom náhodne.
	</P>
	
	<P Align="center">
		<IMG Src="./images/KetchupStart.png" Alt=""></br>
		<I>Roboty stoja na začiatku na pozícii A3, E3. Dve plechovky sú vždy na C2 a C4, Ďalšie dve na niektorom zo žltých políčok, napríklad B2 a D4.</I>
	</P>

	<h3>Robot - skladník</h3>

	<P>
		Robot musí byť autonómny. Počas súťažného kola nie je dovolené do jeho činnosti nijako zasahovať. 
	</P>
	
	<P>
		Dĺžka a šírka robota nesmie prekročiť 30 cm. Ak robot mení počas činnosti svoje rozmery, v žiadnom okamihu nesmie prekročiť 30×30 cm. 
		Výška nie je obmedzená. Vezmite do úvahy aj rozmery skladu a miesto potrebné na otáčanie. 
	</P>
	
	<P>
		Robot sa nemusí pohybovať len po čiarach, tie sú tam len na uľahčenie navigácie.
		Pri pohybe nesmie zanechávať žiadne stopy a značky. Na dráhe nesmie ostať ani žiadna jeho súčasť. 
	</P>

	<h3>Činnosť robota skladníka</h3>

	<P>
		Základnou úlohou je identifikovať v sklade plechovky s kečupom a dopraviť ich na domácu čiaru. 
		Je povolené manipulovať aj s plechovkami, ktoré si už súper dopravil domov. Je však pritom zakázané poškodzovať súperovho robota.
	</P>
	
	<P>
		Pred štartom obaja súperi postavia roboty na počiatočné pozície a na signál rozhodcu ich aktivujú. 
		Následne bez meškania odstúpia a ďalej do činnosti robota nezasahujú. Po vypršaní časového limitu obaja súperi robota bez meškania deaktivujú. 
	</P>
	
	<P>
		Po skončení kola sa hodnotí počet plechoviek na domácej čiare robota. 
		Plechovka sa započíta robotovi len vtedy, ak sa aspoň malou časťou dotýka domácej čiary robota. Nemusí sa nachádzať v priesečníku. 
	</P>
	
	<P>
		Počet plechoviek na domácej čiare predstavuje bodový zisk robota v danom kole. 
		Súťažiť sa bude systémom každý s každým (v prípade vyššieho počtu účastníkov v skupinách).
	</P>
	
	<P Align="center">
		<IMG Src="./images/KetchupEnd.png" Alt=""></br>
		<I>Vyhral zelený robot - má na domácej čiare 3 plechovky, modrý má len jednu. Po skončení kola môže robot skončiť kdekoľvek, nemusí sa vrátiť domov.</I>
	</P>
	
	<P>
		Kečupy sa spočítavajú až po skončení kola, dovtedy si ich môžu roboty vzájomne "vykrádať". 
	</P>

	<h3>Poradie účastníkov</h3>

	<P>
		Poradie bude stanovené tesne pred súťažou.  
	</P>
	
	<P>
		Počas súťaže je možné meniť algoritmus, upravovať a nastavovať parametre robota v závislosti na súperovi, 
		proti ktorému bude súťažiť. 
	</P>

	<h3>Časové limity</h3>
	
	<P>
		Ak sa súťažiaci nedostaví do 1 minúty po výzve na štart, stráca právo absolvovať kolo. Každé kolo trvá 3 minúty. 
		V prípade, že s tým súhlasia obaja súperi, môže byť ukončené aj skôr. 
	</P>

	<h3>Hodnotenie a ceny</h3>
	
	<P>
		Víťazom turnaja sa stane robot s najvyšším bodovým ziskom. 
		Ak počas súťaže nezíska žiaden robot ani jeden bod, porota určí víťaza na základe celkovej úspešnosti - 
		napr. ako blízko k cieľu sa podarilo plechovku dostať, či bol pohyb po sklade koordinovaný, alebo len náhodný, a pod. 
	</P>
	
	<P>
		Okrem toho môže porota udeliť ďalšie ceny podľa vlastého uváženia - napr. za najobjavnejšiu konštrukciu, najelegantnejšiu konštrukciu a pod.
	</P>
	
