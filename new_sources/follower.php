	<H2>Pravidlá kategórie</BR></BR> Stopár</H2>
	</BR>
	<H3>Súťažná úloha </H3>
	<P>
		Zostrojiť autonómneho mobilného robota, ktorý prejde po určenej dráhe a v časovom limite do cieľa. 
		Smer a trasa je daná tmavým pruhom, na dráhe sú umiestnené rozličné prekážky. 
	</P>

	<H3>Dráha</H3>
	
	<p>
		Podklad dráhy bude svetlý (biely) s tmavou (čiernou) vodiacou čiarou šírky 15+/-1 mm. 
		Celková dĺžka dráhy nepresiahne 20 m. Materiál dráhy je drevotrieska.
	</p>
	
	<p>
		Najmenší polomer oblúkov na trati bude 5 cm. Celkové prevýšenie nepresiahne 3 cm, maximálne stúpanie a klesanie je 5% (s výnimkou
		<A HREF="./images/LinefollowerBridge.png" target="_blank">mostíka</A>). 
		Na dráhe, podobne ako v reálnom svete, sa môžu vyskytovať náhodné prekážky (prerušená vodiaca čiara, prekážka na vodiacej čiare, zmena osvetlenia čiary a iné). 

	<p>
		Úlohou robota nie je hľadať správnu cestu, ale výhradne sledovať namaľovanú čiaru. 
		Dráha sa počas súťaže nemení, takže robot sa môže učiť a napr. rovné úseky pri ďalšej jazde prechádzať rýchlejšie.
	</p>
	
	<p>
		Robot vyštartuje z tzv. štartovacieho poľa, ktoré sa nachádza 30 cm pred pomyslenou štartovacou čiarou, ktorá je realizovaná infračerveným lúčom časomiery. 
		Po prerušení svetelného lúča (10 mm nad povrchom dráhy) ľubovoľnou časťou robota sa začne merať čas. 
		Dráha bude uzavretá (okruh) a v priestore štartovacieho poľa (a teda aj cieľa) bude cez čiernu čiaru nalepený kolmý pásik dlhý cca 10 cm, 
		ktorý bude znamenať začiatok a koniec dráhy. Súťažiaci, ktorým by pásik vadil, môžu robota na dráhu postaviť až za ním. 
		Tak môžu roboty lepšie využiť znalosti získané z predošlých kôl. 
	</p>
	
	<P>
		Časomiera sa zastaví po prerušení podobného lúča na konci dráhy.
	</P>
	
	<P>
		Súťažná dráha nemá mantinely. 
		Okolo čiary bude na každej strane bezpečný pás široký 15 cm, ale napriek tomu sa odporúča vo vlastnom záujme robota vybaviť bezpečnostným zariadením proti pádu zo stola. 
		Organizátor nezaručuje, že okolo dráh bude voľný priestor. Preto musia súťažiaci dôkladne vyhodnocovať odozvy zo senzorov, najmä pri okrajoch dráh. 
	</P>


