	<H3>Činnosť robota</H3>

	<P>
		Po umiestnení robota do štartovacieho kruhu a jeho zapnutí súťažiaci nesmie do jeho činnosti nijakým spôsobom zasahovať. 
		Robot musí byť riadený výlučne vstavanými elektronickými obvodmi. 
	</P>
	
	<P>
		Po prekročení štartovacej čiary robot nesmie opustiť dráhu vyznačenú tmavou čiarou s výnimkou obchádzania prekážky. 
		Pri pohybe nesmie zanechávať žiadne stopy a značky. Na dráhe nesmie ostať ani žiadna jeho súčasť.
	</P>
	
	<P>
		Pri poškodení súťažnej dráhy je robot diskvalifikovaný.
	</P>
	
	<H3>Rozmery a veľkosť robota</H3>

	<P>
		Šírka a výška robota nesmie presiahnuť 20 cm. Dĺžka nie je obmedzená. Uvedomte si aj obmedzenia dané tunelom a polomermi oblúkov na dráhe.
	</P>
	
	<P>
		Minimálne rozmery nie sú obmedzené. Hmotnosť nie je obmedzená. 
	</P>

	<H3>Poradie účastníkov a priebeh súťaže</H3>

	<P>
		Poradie súťažiacich bude stanovené tesne pred súťažou. Účastníci musia absolvovať predpísanú dráhu v stanovenom poradí. 
		Každý súťažiaci robot môže prejsť dráhu trikrát vo vylosovanom poradí. 
		Čas medzi pokusmi môže účastník využiť na ľubovoľné opravy a úpravy. 
		Ak sa však nedostaví do 1 minúty po výzve na štart, stráca právo absolvovať pokus. 
		V prípade, že sa prihlási veľa súťažiacich, porota môže znížiť počet povolených pokusov, prípadne vyhlásiť kvalifikačné kolo. 
	</P>
	
	<H3>Časové limity</H3>

	<P>
		Na prejdenie dráhy je stanovený časový limit 3 minúty. Po troch minútach sa meranie času zastaví a pokus sa zruší. 
		Aj napriek tomuto handicapu však robot nie je diskvalifikovaný. Zastavenie merania času nemá nijaký vplyv na ostatné pokusy. 
	</P>

	<H3>Hodnotenie a ceny</H3>

	<P>
		Víťazom sa stane robot s najnižším dosiahnutým časom zo všetkých pokusov. 
		Okrem toho môže porota udeliť cenu za najlepšiu konštrukciu a najelegantnejšiu konštrukciu. 
	</P>
	
