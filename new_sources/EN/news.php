			<H2>Categories</H2>
			<P>
				<A HREF="index.php?page=rules&type=follower"><IMG Src="images/stopar.jpg" Width=150 Height=105 Align="right" Alt="Stopar" Border=0></A>
				<H3><A href="index.php?page=rules&type=follower">Linefollower</A></H3>
				The task is to construct an autonomous robot that will
				complete the racing track and negotiate all its pitfalls.
				It contains several obstacles - for example a bridge, tunnel to pass
				through and a brick to be avoided.</BR>
				<A href="index.php?page=rules&type=follower">Details...</A>
				</BR></BR>
			</P>

			<P>
				<A HREF="index.php?page=rules&type=umouse"><IMG Src="images/umouse.jpg" Width=150 Height=105 Align="right" Alt="Micromouse" Border=0></A>
				<H3><A HREF="index.php?page=rules&type=umouse">Micromouse</A></H3>
				The task is to construct a robot that can autonomously solve
				a maze and achieve the goal in the shortest time. When the
				robot tracks the right hand wall it will solve the maze, but
				not in the shortest possible time.</BR>
				<A href="index.php?page=rules&type=umouse">Details...</A>
				</BR></BR>
			</P>

			<P>
				<A HREF="index.php?page=rules&type=ketchup"><IMG Src="images/ketchup.jpg" Width=150 Height=105 Align="right" Alt="Ketchup" Border=0></A>
				<H3><A href="index.php?page=rules&type=ketchup">Ketchup house</A></H3>
				In this category the task is to localize and sort ketchup cans in
				the storage. Two robots compete, winner is faster and more precise robot.  </BR>
				<A href="index.php?page=rules&type=ketchup">Details...</A>
				</BR></BR>
			</P>

			<P>
				<A href="index.php?page=rules&type=freestyle"><IMG Src="images/freestyle.jpg" Width=150 Height=105 Align="right" Alt="Volna jazda" Border=0></A>
				<H3><A href="index.php?page=rules&type=freestyle">Freestyle</A></H3>
				In this category, competitors are encouraged to demonstrate
				their robotics creations which do not fit into other categories.
				The winner is appointed according to the jury judgment.</BR>
				<A href="index.php?page=rules&type=freestyle">Details...</A>
				</BR></BR></BR>
			</P>
			
